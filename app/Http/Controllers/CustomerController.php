     */
    public function index()
    {
        $freelancer = request('freelancer');
        if($freelancer){
          $list = Customer::whereFreelancerId($freelancer);
          return view('dev.customer.index')->with([
            'list'        =>  $list->paginate(),
            'freelancer'  =>  $freelancer
          ]);
        }
        return view('dev.customer.report')->with([
          'list'  =>  Customer::paginate(),
        ]);
    }

